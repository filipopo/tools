const timeconvertor = require('./timeconvertor');

test('Testing 12hour to 24hour conversion', () => {
  const input = [
    '00:01:00AM',
    '01:01:00AM',
    '02:01:00AM',
    '03:01:00AM',
    '04:01:00AM',
    '05:01:00AM',
    '06:01:00AM',
    '07:01:00AM',
    '08:01:00AM',
    '09:01:00AM',
    '10:01:00AM',
    '11:01:00AM',
    '12:01:00PM',
    '01:01:00PM',
    '02:01:00PM',
    '03:01:00PM',
    '04:01:00PM',
    '05:01:00PM',
    '06:01:00PM',
    '07:01:00PM',
    '08:01:00PM',
    '09:01:00PM',
    '10:01:00PM',
    '11:01:00PM',
    '12:01:00AM'
  ];

  const output = [
    '00:01:00', 
    '01:01:00', 
    '02:01:00', 
    '03:01:00', 
    '04:01:00', 
    '05:01:00', 
    '06:01:00', 
    '07:01:00', 
    '08:01:00', 
    '09:01:00', 
    '10:01:00', 
    '11:01:00', 
    '12:01:00', 
    '13:01:00', 
    '14:01:00', 
    '15:01:00', 
    '16:01:00', 
    '17:01:00', 
    '18:01:00', 
    '19:01:00', 
    '20:01:00', 
    '21:01:00', 
    '22:01:00', 
    '23:01:00', 
    '00:01:00'
  ];

  for (let i = 0; i < input.length; i++)
    expect(timeconvertor.to24Hour(input[i])).toBe(output[i]);
});

